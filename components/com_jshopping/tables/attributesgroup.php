defined('_JEXEC') or die( 'Restricted access' );
jimport('joomla.application.component.model');

class jshopAttributesGroup extends JTableAvto
{
    
    public function __construct(&$_db)
    {
        parent::__construct('#__jshopping_attr_groups', 'id', $_db);
    }

	public function bind($src, $ignore = Array())
	{
		$fields = (parent::getTableFields()) ?: [];		
		foreach ($fields as $key=>$value){
			
			if ((!isset($src[$key]))&&($value->Extra!="auto_increment")){
				if ((strtoupper(substr($value->Type,0,4))=='TEXT')||(strtoupper(substr($value->Type,0,4))=='VARC')){					
					$src[$key]="";
				}
			}
			
			if ((($src[$key]==""))&&($value->Extra!="auto_increment")){
				if ((strtoupper(substr($value->Type,0,4))!='TEXT')&&(strtoupper(substr($value->Type,0,4))!='VARC')){					
					$src[$key]=0;
				}
			}
						
		}
		return parent::bind($src, $ignore);
	}
	
    public function getList()
    {
        $lang = JSFactory::getLang(); 
        $select = ['id', '`' . $lang->get('name') . '` as name', 'ordering'];

        $modelOfAttrsGroupsFront = JSFactory::getModel('AttrsGroupsFront');
        return $modelOfAttrsGroupsFront->select($select, [], 'ORDER BY ordering');
    }
}